package IOT_System;

public class Shortcuts {

    public void printString(String printString) {
        System.out.println(printString);
        /* PRINTS TO CONSOLES */
    }
    public void printStrArr(String[] printStrArr) {
        System.out.println(printStrArr);
        /* PRINTS TO CONSOLES */
    }

    public void printInteger(int printInteger) {
        System.out.println(printInteger);
        /* PRINTS TO CONSOLES */
    }

    public void printDouble(double printDouble) {
        System.out.println(printDouble);
        /* PRINTS TO CONSOLES */
    }
    public void printFloat(float printFloat) {
        System.out.println(printFloat);
        /* PRINTS TO CONSOLES */
    }



}
